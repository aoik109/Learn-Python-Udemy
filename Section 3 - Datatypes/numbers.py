### Numbers
#### integers and floats
print(1 + 1.1) # integer + float = float
print(10 / 5) # integer / integer = float
print(float(2)) # converts arg into a float
print(int(5.1)) # converts arg into an int; truncates the float (cuts off after .)
print(round(5.9)) # rounds to nearest whole number
print(1.1 * 3) # should be 3.3 but prints 3.00000000003